def kgToLb(kg):
    return kg * 2.20462

def lbToKg(lb):
    return lb / 2.20462

def lbfToN(lbf):
    return lbf * 4.44822

def ftToM(ft):
    return ft / 3.28084

def mToFt(m):
    return m * 3.28084

def kmToMi(km):
    return km / 1.60934

def paToPsi(pa):
    return pa / 6894.76

def kgm3ToLbft3(kgm3):
    return kgm3 / 16.0185

def nToLbf(n):
    return n / 4.44822

def lbmToSlug(lbm):
    return lbm / 32.174049

def psfToPsi(psf):
    return psf / 144
